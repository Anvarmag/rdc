/**
 * ==========================================================================
 * РДЦ - Радиологический дата-центр
 * ФАЙЛ: cookie-consent.js
 * НАЗНАЧЕНИЕ: Управление баннером уведомления о файлах cookie
 * ==========================================================================
 *
 * ЧТО ЭТО ТАКОЕ:
 * Баннер, который уведомляет пользователя об использовании cookies.
 * Появляется через 2 секунды после загрузки страницы (если ранее не был принят).
 * После нажатия кнопки «Принять» баннер скрывается и запоминает выбор в localStorage.
 *
 * КАК ЭТО РАБОТАЕТ:
 * 1. При загрузке DOM проверяется localStorage на наличие ключа 'cookieConsent'
 * 2. Если ключа нет — баннер показывается через 2 секунды (с анимацией)
 * 3. При клике на «Принять» — ключ сохраняется, баннер скрывается
 * 4. При следующих визитах баннер больше не появляется
 *
 * ТРЕБОВАНИЯ:
 * В HTML должны быть элементы с ID:
 * - #cookie-banner  — контейнер баннера (изначально display:none)
 * - #accept-cookies — кнопка «Принять»
 *
 * ==========================================================================
 */

// Ждём полной загрузки DOM перед выполнением кода
document.addEventListener("DOMContentLoaded", function () {

    // Находим DOM-элементы баннера и кнопки принятия
    const banner = document.getElementById('cookie-banner');   // Контейнер всего баннера
    const btn = document.getElementById('accept-cookies');     // Кнопка «Принять»

    // Проверяем, принимал ли пользователь cookies ранее
    // localStorage сохраняет данные между сессиями (даже после закрытия браузера)
    if (!localStorage.getItem('cookieConsent')) {
        // Пользователь ещё не принимал — показываем баннер с задержкой 2 секунды
        // Задержка нужна, чтобы баннер не появлялся резко сразу при загрузке
        setTimeout(() => {
            banner.style.display = 'block';           // Делаем баннер видимым
            banner.classList.add('animate-enter');      // Добавляем CSS-класс для анимации появления
        }, 2000); // 2000 мс = 2 секунды задержки
    }

    // Если кнопка «Принять» существует — вешаем на неё обработчик клика
    if (btn) {
        btn.addEventListener('click', function () {
            // Сохраняем согласие пользователя в localStorage
            // При следующем визите localStorage.getItem('cookieConsent') вернёт 'true'
            localStorage.setItem('cookieConsent', 'true');

            // Скрываем баннер
            banner.style.display = 'none';
        });
    }
});